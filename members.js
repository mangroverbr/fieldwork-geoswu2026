function renderFieldGroups(keyword = "") {
    const container = document.getElementById("groupsContainer");
    if (!container) return;

    const searchText = keyword.trim().toLowerCase();

    const filteredGroups = fieldGroups.filter(group => {
        const groupText = [
            `กลุ่มที่ ${group.group}`,
            `กลุ่ม ${group.group}`,
            `${group.group}`,
            ...Object.entries(group.members).flatMap(([year, names]) => [year, ...names])
        ].join(" ").toLowerCase();

        return groupText.includes(searchText);
    });

    if (filteredGroups.length === 0) {
        container.innerHTML = `<div class="no-result">ไม่พบรายชื่อที่ค้นหา</div>`;
        return;
    }

    container.innerHTML = filteredGroups.map(group => {
        const yearSections = Object.entries(group.members).map(([year, names]) => {
            const listItems = names.map(name => `<li>${name}</li>`).join("");
            return `
                <h4>${year}</h4>
                <ul>${listItems}</ul>
            `;
        }).join("");

        return `
            <div class="group-card">
                <h3>
                    <span>กลุ่มที่ ${group.group}</span>
                    <span class="group-count">${group.total} คน</span>
                </h3>
                ${yearSections}
            </div>
        `;
    }).join("");
}

function initGroupSearch() {
    const searchInput = document.getElementById("groupSearch");
    if (!searchInput) return;

    searchInput.addEventListener("input", e => {
        renderFieldGroups(e.target.value);
    });
}

